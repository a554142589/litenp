#!/usr/bin/env python3
"""Merge C++ and NumPy benchmark outputs into a Phase 5 pass/fail report."""

from __future__ import annotations

import argparse
import json
import re
from dataclasses import dataclass
from pathlib import Path
from typing import Dict, Iterable, Optional


@dataclass
class Timing:
    best_ms: Optional[float] = None
    median_ms: Optional[float] = None


@dataclass
class CppRow:
    litenp: Timing
    eigen: Timing
    torch: Timing


def parse_timing(text: str) -> Timing:
    if text == "n/a":
        return Timing()
    if "/" in text:
        best, median = text.split("/", 1)
        return Timing(float(best), float(median))
    value = float(text)
    return Timing(value, value)


def parse_cpp(path: Path) -> Dict[str, CppRow]:
    rows: Dict[str, CppRow] = {}
    matrix_re = re.compile(
        r"^(.*?)\s+"
        r"([0-9.]+/[0-9.]+|n/a)\s+"
        r"([0-9.]+/[0-9.]+|n/a)\s+"
        r"([0-9.]+/[0-9.]+|n/a)\s+"
        r"([0-9.]+x|n/a)\s+"
        r"([0-9.]+x|n/a)\s+"
    )
    simple_re = re.compile(r"^(.*?)\s+([0-9]+(?:\.[0-9]+)?)\s+[-+0-9.eE]+$")

    in_simple_table = False
    for raw in path.read_text().splitlines():
        line = raw.rstrip()
        if not line:
            in_simple_table = False
            continue
        if line.startswith("case") and "best ms" in line:
            in_simple_table = True
            continue
        if line.startswith("case") and "litenp b/m" in line:
            in_simple_table = False
            continue
        m = matrix_re.match(line)
        if m:
            name = m.group(1).strip()
            rows[name] = CppRow(parse_timing(m.group(2)), parse_timing(m.group(3)), parse_timing(m.group(4)))
            continue
        if in_simple_table:
            m = simple_re.match(line)
            if m:
                name = m.group(1).strip()
                rows[name] = CppRow(parse_timing(m.group(2)), Timing(), Timing())
    return rows


def parse_numpy(path: Path) -> Dict[str, Timing]:
    payload = json.loads(path.read_text())
    return {
        row["name"]: Timing(float(row["best_ms"]), float(row["median_ms"]))
        for row in payload.get("rows", [])
    }


def ratio(base: Timing, litenp: Timing) -> Optional[float]:
    if base.best_ms is None or litenp.best_ms is None or litenp.best_ms <= 0.0:
        return None
    return base.best_ms / litenp.best_ms


def format_timing(t: Timing) -> str:
    if t.best_ms is None:
        return "n/a"
    return f"{t.best_ms:.6f}/{t.median_ms:.6f}"


def format_ratio(value: Optional[float]) -> str:
    if value is None:
        return "n/a"
    return f"{value:.2f}x"


def row_status(required: bool, ratios: Iterable[tuple[str, bool, Optional[float]]]) -> str:
    missing = [name for name, needed, value in ratios if needed and value is None]
    failing = [name for name, needed, value in ratios if needed and value is not None and value < 1.0]
    if missing:
        return "uncovered"
    if failing:
        return "fail"
    return "pass" if required else "diagnostic"


def write_report(manifest: dict, cpp_rows: Dict[str, CppRow], np_rows: Dict[str, Timing], out: Path) -> None:
    lines = [
        "# litenp Phase 5 Benchmark Comparison",
        "",
        "Generated by `tools/compare_benchmarks.py`.",
        "",
        "| status | row | operator | litenp b/m | NumPy b/m | Eigen b/m | libtorch b/m | NumPy/lite | E/lite | T/lite |",
        "| --- | --- | --- | ---: | ---: | ---: | ---: | ---: | ---: | ---: |",
    ]
    counts = {"pass": 0, "fail": 0, "uncovered": 0, "diagnostic": 0}

    for item in manifest["rows"]:
        cpp = cpp_rows.get(item["cpp"])
        np_timing = np_rows.get(item.get("numpy", ""))
        lite = cpp.litenp if cpp else Timing()
        cpp_baselines = item.get("cpp_baselines", {})
        eigen_timing = cpp_rows.get(cpp_baselines.get("eigen", ""), CppRow(Timing(), Timing(), Timing())).litenp
        torch_timing = cpp_rows.get(cpp_baselines.get("torch", ""), CppRow(Timing(), Timing(), Timing())).litenp
        if cpp_baselines.get("eigen") is None and cpp:
            eigen_timing = cpp.eigen
        if cpp_baselines.get("torch") is None and cpp:
            torch_timing = cpp.torch
        baselines = item.get("baselines", {})
        np_ratio = ratio(np_timing or Timing(), lite)
        e_ratio = ratio(eigen_timing, lite)
        t_ratio = ratio(torch_timing, lite)
        status = row_status(
            bool(item.get("required", True)),
            (
                ("numpy", bool(baselines.get("numpy")), np_ratio),
                ("eigen", bool(baselines.get("eigen")), e_ratio),
                ("torch", bool(baselines.get("torch")), t_ratio),
            ),
        )
        counts[status] += 1
        lines.append(
            "| "
            + " | ".join(
                [
                    status,
                    item["name"],
                    item.get("operator", ""),
                    format_timing(lite),
                    format_timing(np_timing or Timing()),
                    format_timing(eigen_timing),
                    format_timing(torch_timing),
                    format_ratio(np_ratio),
                    format_ratio(e_ratio),
                    format_ratio(t_ratio),
                ]
            )
            + " |"
        )

    completion_note = (
        "All required rows pass; the benchmark gate is complete."
        if counts["fail"] == 0 and counts["uncovered"] == 0
        else "The active goal is not complete while any required row is `fail` or `uncovered`."
    )
    lines.extend([
        "",
        "## Summary",
        "",
        f"- pass: {counts['pass']}",
        f"- fail: {counts['fail']}",
        f"- uncovered: {counts['uncovered']}",
        f"- diagnostic: {counts['diagnostic']}",
        "",
        completion_note,
        "",
    ])
    out.write_text("\n".join(lines))


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--manifest", required=True, type=Path)
    parser.add_argument("--cpp", required=True, type=Path)
    parser.add_argument("--numpy", required=True, type=Path)
    parser.add_argument("--out", required=True, type=Path)
    args = parser.parse_args()

    manifest = json.loads(args.manifest.read_text())
    cpp_rows = parse_cpp(args.cpp)
    np_rows = parse_numpy(args.numpy)
    write_report(manifest, cpp_rows, np_rows, args.out)


if __name__ == "__main__":
    main()
